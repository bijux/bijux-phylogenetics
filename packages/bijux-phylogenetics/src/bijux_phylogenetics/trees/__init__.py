def __getattr__(name: str):
    if name in _LAZY_UNCERTAINTY_FIGURE_EXPORTS:
        module = import_module(".uncertainty", __name__)
        return getattr(module, name)
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")
